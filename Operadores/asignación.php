<?php
// Operadores de Asignación
    $x = 10;
    $y = 5;

    $x += $y; // $x = $x + $y;
    echo "Operador de Asignación (+=): " . $x . "<br>";

    $x -= $y; // $x = $x - $y;
    echo "Operador de Asignación (-=): " . $x . "<br>";

    $x *= $y; // $x = $x * $y;
    echo "Operador de Asignación (*=): " . $x . "<br>";

    $x /= $y; // $x = $x / $y;
    echo "Operador de Asignación (/=): " . $x . "<br>";

    echo "<br>";
?>